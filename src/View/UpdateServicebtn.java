    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            int x = JOptionPane.showConfirmDialog(null, "Chắc hok ?", "Are you sure about that ?", JOptionPane.YES_NO_OPTION);
            if (x == 0) {
                String sql = "UPDATE hotelmanagement.dichvuphong SET soluong = ? \n"
                        + "WHERE mathuephong = ? \n"
                        + " and madv = (select madv from dichvu where tendv = ? )  \n"
                        + "and ngaydung = ? \n"
                        + "and maphong = ?";
                PreparedStatement ps = DbConn.getConnection().prepareStatement(sql);
                ps.setString(1, txtSoluong.getText());
                ps.setString(2, String.valueOf(mathuephongOwO));
                ps.setString(3, txtTendv.getText());
                ps.setString(4, txtNgaysudung.getText());
                ps.setString(5, String.valueOf(maphongOwO));
                ps.executeUpdate();
                ps.close();
                JOptionPane.showMessageDialog(null, "Đã sửa dịch vụ");

            }
        } catch (Exception U) {
            System.err.println(U);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

