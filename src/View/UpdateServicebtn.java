    private void delbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbtnActionPerformed
        try {
            String sql = "DELETE FROM dichvuphong  WHERE \n"
                    + "                    mathuephong = ? and madv = (select madv from dichvu where tendv = ? ) and ngaydung =? and maphong =?";
            PreparedStatement ps = DbConn.getConnection().prepareStatement(sql);
            ps.setInt(1, mathuephongOwO);
            ps.setString(2, txtTendv.getText());
            ps.setString(3, txtNgaysudung.getText());
            ps.setInt(4, maphongOwO);
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(null, "Đã xóa dịch vụ");
        } catch (Exception e) {
        }

    }//GEN-LAST:event_delbtnActionPerformed

